package dsa.dp.partition;

import java.util.Arrays;

/**
 * வீடுகளை k குழுக்களாகப் பிரித்து, ஒவ்வொரு குழுவிற்கும் அஞ்சல் பெட்டியை
 * அந்த குழுவின் median (நடுநிலை) இடத்தில் வைத்து,
 * அனைத்து வீடுகளிலிருந்தும் அவற்றின் அஞ்சல் பெட்டிக்கான தூரங்களின்
 * கூட்டுத்தொகையைக் குறைக்கும் குறைந்தபட்ச மதிப்பைக் கணக்கிடும்.
 *
 * <p>முக்கிய யோசனை:
 * <ul>
 *   <li>வீடுகளை அவற்றின் இருப்பிடத்தின் அடிப்படையில் ஏறு வரிசையில் அடுக்கிக் கொள்ளவும்.</li>
 *   <li>தொடர்ச்சியான வீடுகளை மட்டுமே ஒரு குழுவாகப் பிரிக்க முடியும் (contiguous grouping).</li>
 *   <li>ஒரு குழுவில் உள்ள வீடுகளின் median இடத்தில் அஞ்சல் பெட்டியை வைப்பதே குறைந்தபட்ச தூரத்தைத் தரும்.</li>
 *   <li>அனைத்து சாத்தியமான பிரிவுகளையும் சோதித்து, சிறந்ததைத் தேர்வு செய்ய Partition DP பயன்படுத்தப்படுகிறது.</li>
 * </ul>
 *
 * <p>மறுநிகழ்வுத் தொடர்பு (Recurrence Relation):
 * <pre>
 * dp[i][k] = min( cost(i, j) + dp[j+1][k-1] ) for all j from i to n-1
 * </pre>
 * இங்கு:
 * <ul>
 *   <li>dp[i][k] - i-ஆம் இடத்திலிருந்து தொடங்கி k அஞ்சல் பெட்டிகள் உள்ளபோதைய குறைந்தபட்ச மொத்த தூரம்</li>
 *   <li>cost(i, j) - i முதல் j வரையிலான வீடுகளை ஒரே குழுவாக எடுத்துக்கொண்டால் வரும் மொத்த தூரம்</li>
 * </ul>
 */
public class AllocateMailboxes {

    int[] houses;  // வீடுகளின் இருப்பிடங்களைச் சேமிக்கும் அணி (sort செய்யப்பட்டது)
    int n;         // மொத்த வீடுகளின் எண்ணிக்கை
    int[][] dp;    // DP முடிவுகளைச் சேமிக்கும் அணி (memoization)

    /**
     * முக்கிய செயலி முறை - வீடுகளை k குழுக்களாகப் பிரித்து குறைந்தபட்ச மொத்த தூரத்தைக் கணக்கிடும்
     *
     * @param houses வீடுகளின் இருப்பிடங்களைக் கொண்ட அணி
     * @param k      எத்தனை அஞ்சல் பெட்டிகள் (குழுக்கள்) ஒதுக்க வேண்டும்
     * @return       குறைந்தபட்ச மொத்த தூரம்
     */
    public int minDistance(int[] houses, int k) {
        Arrays.sort(houses);  // வீடுகளை ஏறு வரிசையில் அடுக்கிக் கொள்ளுங்கள் - இது முக்கியம்!
        this.houses = houses;
        this.n = houses.length;
        this.dp = new int[n][k + 1];

        // DP அணியை -1 ஆல் நிரப்புக (இதுவரை கணக்கிடப்படவில்லை என்பதைக் குறிக்க)
        for (int[] arr : dp) {
            Arrays.fill(arr, -1);
        }

        return solve(0, k);
    }

    /**
     * i-ஆம் இடத்திலிருந்து தொடங்கி k அஞ்சல் பெட்டிகளை ஒதுக்கும்போது கிடைக்கும்
     * குறைந்தபட்ச மொத்த தூரத்தைக் கணக்கிடும் மறுநிகழ்வு (recursive) முறை
     *
     * <p>செயல்முறை:
     * <ol>
     *   <li>தற்போதைய i-ஆம் இடத்திலிருந்து ஒரு குழுவை உருவாக்கி (i முதல் j வரை),</li>
     *   <li>அந்தக் குழுவிற்கான cost-ஐக் கணக்கிட்டு,</li>
     *   <li>மீதமுள்ள வீடுகளுக்கு (j+1 முதல்) k-1 அஞ்சல் பெட்டிகளை ஒதுக்கி,</li>
     *   <li>இரண்டையும் கூட்டி, அனைத்து j-க்கும் சோதித்து சிறந்ததைத் தேர்வு செய்கிறது.</li>
     * </ol>
     *
     * @param i தற்போதைய தொடக்கக் குறியீடு (index) - இங்கிருந்து வீடுகளை எடுத்துக்கொள்ள வேண்டும்
     * @param k இன்னும் ஒதுக்கப்பட வேண்டிய அஞ்சல் பெட்டிகளின் எண்ணிக்கை
     * @return  குறைந்தபட்ச மொத்த தூரம் (சாத்தியமில்லையென்றால் Integer.MAX_VALUE)
     */
    int solve(int i, int k) {
        // வெற்றிகரமான முடிவு: அனைத்து வீடுகளையும் பார்த்துவிட்டோம், மீதி அஞ்சல் பெட்டிகளும் இல்லை
        if (i == this.n && k == 0) return 0;

        // தோல்வியுற்ற முடிவு: வீடுகள் மீதமுள்ளன, ஆனால் அஞ்சல் பெட்டிகள் இல்லை (அல்லது நேர்மாறு)
        if (i == n || k == 0) return Integer.MAX_VALUE;

        // ஏற்கனவே கணக்கிட்டிருந்தால், அதைத் திருப்பித் தருக (memoization)
        if (dp[i][k] != -1) return dp[i][k];

        int ans = Integer.MAX_VALUE;

        // j என்பது தற்போதைய குழுவின் இறுதிக் குறியீடு
        // i முதல் j வரையிலான வீடுகளை ஒரே குழுவாக எடுத்துக்கொள்கிறோம்
        for (int j = i; j < n; j++) {
            int currentCost = cost(i, j);        // இந்தக் குழுவிற்கான தூரச் செலவு
            int next = solve(j + 1, k - 1);      // மீதமுள்ள வீடுகளுக்கான செலவு

            // next சாத்தியமானதாக இருந்தால் மட்டுமே (Integer.MAX_VALUE இல்லை என்றால்)
            if (next != Integer.MAX_VALUE) {
                ans = Math.min(ans, currentCost + next);  // சிறந்ததைத் தேர்வு செய்க
            }
        }

        // கணக்கிட்ட முடிவைச் சேமித்து, அதைத் திருப்பித் தருக
        return dp[i][k] = ans;
    }

    /**
     * i முதல் j வரையிலான வீடுகளை ஒரே குழுவாக எடுத்துக் கொண்டால்,
     * அந்த குழுவிற்கு median (நடுநிலை) இடத்தில் அஞ்சல் பெட்டியை வைப்பதன்
     * மூலம் கிடைக்கும் மொத்த தூரக் கூட்டுத்தொகையைக் கணக்கிடுகிறது.
     *
     * <p>ஏன் median?
     * - ஒரு புள்ளிகளின் தொகுப்பிற்கு, முழுமையான தூரங்களின் கூட்டுத்தொகையைக் குறைப்பது median ஆகும்.
     * - சராசரி (mean) முழு வர்க்கத் தூரங்களுக்கு (squared distances) உகந்தது.
     * - முழுமையான தூரங்களுக்கு (absolute distances) median உகந்தது.
     *
     * <p>குறிப்பு: வீடுகள் ஏறு வரிசையில் அடுக்கப்பட்டுள்ளதால், median = houses[(i+j)/2]
     *
     * @param i குழுவின் தொடக்கக் குறியீடு (inclusive)
     * @param j குழுவின் இறுதிக் குறியீடு (inclusive)
     * @return  median-க்கான மொத்த முழுமையான தூரக் கூட்டுத்தொகை
     */
    int cost(int i, int j) {
        int m = (i + j) / 2;           // median-இன் குறியீடு (index)
        int total = 0;
        for (int x = i; x <= j; x++) {
            total += Math.abs(houses[x] - houses[m]);  // ஒவ்வொரு வீட்டிற்கும் median-க்கும் உள்ள தூரம்
        }
        return total;
    }

    /**
     * எடுத்துக்காட்டு பயன்பாட்டிற்கான main முறை
     */
    public static void main(String[] args) {
        int[] houses = {1, 4, 8, 10, 20};
        int k = 3;
        // எதிர்பார்க்கப்படும் முடிவு: 5 (எடுத்துக்காட்டு: [1,4], [8,10], [20] → தூரங்கள்: 3 + 2 + 0 = 5)
        System.out.println(new AllocateMailboxes().minDistance(houses, k));
    }
}